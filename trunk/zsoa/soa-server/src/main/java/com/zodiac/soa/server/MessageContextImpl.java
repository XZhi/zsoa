package com.zodiac.soa.server;

import java.util.Collection;
import java.util.HashMap;
import java.util.Map;
import java.util.Set;
import javax.servlet.ServletContext;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Brian Estrada
 */
public class MessageContextImpl implements javax.xml.ws.handler.MessageContext {

    private Map<String, Object> map;
    
    public MessageContextImpl(
            ServletContext context, 
            HttpServletRequest request,
            HttpServletResponse response) {
        map = new HashMap();
        put(SERVLET_CONTEXT, context);
        put(SERVLET_REQUEST, request);
        put(SERVLET_RESPONSE, response);
    }
    
    @Override
    public void setScope(String name, Scope scope) {
        throw new UnsupportedOperationException("Not supported yet.");
    }

    @Override
    public Scope getScope(String name) {
        throw new UnsupportedOperationException("Not supported yet.");
    }

    @Override
    public int size() {
        return map.size();
    }

    @Override
    public boolean isEmpty() {
        return map.isEmpty();
    }

    @Override
    public boolean containsKey(Object key) {
        return map.containsKey(key);
    }

    @Override
    public boolean containsValue(Object value) {
        return map.containsValue(value);
    }

    @Override
    public Object get(Object key) {
        return map.get(key);
    }

    @Override
    public Object put(String key, Object value) {
        return map.put(key, value);
    }

    @Override
    public Object remove(Object key) {
        return map.remove(key);
    }

    @Override
    public void putAll(Map<? extends String, ? extends Object> m) {
        map.putAll(m);
    }

    @Override
    public void clear() {
        map.clear();
    }

    @Override
    public Set<String> keySet() {
        return map.keySet();
    }

    @Override
    public Collection<Object> values() {
        return map.values();
    }

    @Override
    public Set<Entry<String, Object>> entrySet() {
        return map.entrySet();
    }
    
}
